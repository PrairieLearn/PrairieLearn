import random

import numpy as np
import prairielearn as pl


def generate(data):
    d = random.randint(2, 9)
    data["params"]["d"] = d

    # The "correct answer" stored here is only one example of a valid answer;
    # it is shown to students in the answer panel and used for the "correct"
    # test case.
    data["correct_answers"]["A"] = pl.to_json(np.array([[d, 0], [0, 1]]))


def grade(data):
    # Instead of comparing the submission against the example answer, check
    # whether the submitted matrix satisfies the required property.
    A = np.array(pl.from_json(data["submitted_answers"]["A"]))
    det = np.linalg.det(A)

    if np.isclose(det, data["params"]["d"]):
        score = 1
    else:
        score = 0
        data["feedback"]["A"] = (
            f"The determinant of your matrix is ${det:.2f}$, but it should be ${data['params']['d']}$."
        )

    data["partial_scores"]["A"] = {"score": score, "weight": 1}
    pl.set_weighted_score_data(data)


def test(data):
    if data["test_type"] == "invalid":
        return

    if data["test_type"] == "correct":
        A = np.array(pl.from_json(data["correct_answers"]["A"]))
        score = 1
    else:
        # The identity matrix has determinant 1, which is always incorrect
        # since d is at least 2. We use it instead of the random incorrect
        # matrix generated by pl-matrix-component-input to make the test
        # deterministic.
        A = np.eye(2)
        score = 0

    for i in range(2):
        for j in range(2):
            data["raw_submitted_answers"][f"A{2 * i + j + 1}"] = str(A[i, j])

    data["partial_scores"]["A"] = {"score": score, "weight": 1}
    data["score"] = score
