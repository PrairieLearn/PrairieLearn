import random

import numpy as np
import prairielearn as pl


def generate(data):
    # Each variant asks for a matrix satisfying a different property. The
    # "correct answer" stored here is only one example of a valid answer; it is
    # shown to students in the answer panel and used for the "correct" test case.
    which = random.choice(["nilpotent", "idempotent", "involutory"])

    if which == "nilpotent":
        prompt = "Give an example of a non-zero $2\\times 2$ matrix $A$ such that $A^2$ is the zero matrix."
        example = np.array([[0, 1], [0, 0]])
    elif which == "idempotent":
        prompt = "Give an example of a $2\\times 2$ matrix $A$, other than the zero matrix and the identity matrix, such that $A^2 = A$."
        example = np.array([[1, 1], [0, 0]])
    else:
        prompt = "Give an example of a $2\\times 2$ matrix $A$, other than $I$ and $-I$, such that $A^2 = I$, where $I$ is the $2\\times 2$ identity matrix."
        example = np.array([[0, 1], [1, 0]])

    data["params"]["which"] = which
    data["params"]["prompt"] = prompt
    data["correct_answers"]["A"] = pl.to_json(example)


def grade(data):
    # Instead of comparing the submission against the example answer, check
    # whether the submitted matrix satisfies the required property.
    A = np.array(pl.from_json(data["submitted_answers"]["A"]))
    which = data["params"]["which"]

    if which == "nilpotent":
        excluded = np.allclose(A, np.zeros((2, 2)))
        satisfies = np.allclose(A @ A, np.zeros((2, 2)))
    elif which == "idempotent":
        excluded = np.allclose(A, np.zeros((2, 2))) or np.allclose(A, np.eye(2))
        satisfies = np.allclose(A @ A, A)
    else:
        excluded = np.allclose(A, np.eye(2)) or np.allclose(A, -np.eye(2))
        satisfies = np.allclose(A @ A, np.eye(2))

    if excluded:
        score = 0
        data["feedback"]["A"] = (
            "Your matrix is one of the matrices excluded by the problem statement."
        )
    elif not satisfies:
        score = 0
        data["feedback"]["A"] = "Your matrix does not satisfy the required property."
    else:
        score = 1

    data["partial_scores"]["A"] = {"score": score, "weight": 1}
    pl.set_weighted_score_data(data)


def test(data):
    if data["test_type"] == "invalid":
        return

    if data["test_type"] == "correct":
        A = np.array(pl.from_json(data["correct_answers"]["A"]))
        score = 1
    else:
        # The identity matrix is incorrect for every variant: it does not
        # satisfy the nilpotent property, and it is an excluded answer for the
        # idempotent and involutory properties. We use it instead of the random
        # incorrect matrix generated by pl-matrix-component-input to make the
        # test deterministic.
        A = np.eye(2)
        score = 0

    for i in range(2):
        for j in range(2):
            data["raw_submitted_answers"][f"A{2 * i + j + 1}"] = str(A[i, j])

    data["partial_scores"]["A"] = {"score": score, "weight": 1}
    data["score"] = score
