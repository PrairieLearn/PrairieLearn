#!/usr/bin/env node
// @ts-check

const databaseDiff = require('../lib/databaseDiff');

const yargs = require('yargs')
  .usage('Usage: $0 [options]')
  .describe('db', 'reads a description from the named database')
  .describe('dir', "read a description from a directory that's been generated by `pg-describe.js`")
  .help('h')
  .alias('h', 'help')
  .example(
    '$0 --db postgres --dir db_dump',
    'Diffs the database "postgres" with the description in the direcotry "db_dump"'
  )
  .example(
    '$0 --db postgres --db old_restore',
    'Diffs the database "postgres" with the database "old_restore"'
  )
  .strict();

// TODO: remove cast once `@types/yargs` is fixed
// https://github.com/yargs/yargs/issues/2175
const argv = /** @type {Record<string, any>} */ (yargs.argv);

const options = {
  outputFormat: 'string',
  coloredOutput: process.stdout.isTTY,
};

if (argv.db && !Array.isArray(argv.db) && argv.dir && !Array.isArray(argv.dir)) {
  // Ensure correct ordering for the sake of diffs
  if (process.argv.indexOf('--db') < process.argv.indexOf('--dir')) {
    databaseDiff
      .diffDatabaseAndDirectory(argv.db, argv.dir, options)
      .then(handleResults, handleError);
  } else {
    databaseDiff
      .diffDirectoryAndDatabase(argv.dir, argv.db, options)
      .then(handleResults, handleError);
  }
} else if (argv.db && Array.isArray(argv.db) && argv.db.length === 2 && !argv.dir) {
  databaseDiff.diffDatabases(argv.db[0], argv.db[1], options).then(handleResults, handleError);
} else if (argv.dir && Array.isArray(argv.dir) && argv.dir.length === 2 && !argv.db) {
  databaseDiff.diffDirectories(argv.dir[0], argv.dir[1], options).then(handleResults, handleError);
} else {
  yargs.showHelp();
  process.exit(1);
}

function handleResults(results) {
  process.stdout.write(results);
  process.exit(0);
}

function handleError(err) {
  console.error(err);
  process.exit(1);
}
